
public class Premio {
	private int reintegro;
	private int tres;
	private int cuatro;
	private int cinco;
	
	public void setReintegro(int reintegro) {
		this.reintegro = reintegro;
	}
	public int getReintegro() {
		return reintegro;
	}
	public int getTres() {
		return tres;
	}
	public void setTres(int tres) {
		this.tres = tres;
	}
	public int getCuatro() {
		return cuatro;
	}
	public void setCuatro(int cuatro) {
		this.cuatro = cuatro;
	}
	public int getCinco() {
		return cinco;
	}
	public void setCinco(int cinco) {
		this.cinco = cinco;
	}
}
